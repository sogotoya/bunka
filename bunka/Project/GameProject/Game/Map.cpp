void Map::Draw()
{
	for (int q = 0; q < MAP_HEIGHT; q++) 
	{
		for (int w = 0; w < MAP_WIDTH; w++) 
		{
            if (m_stage_data[q][w] == 0)continue;
            int t = m_stage_data[q][w];
            m_img.SetRect(32*t, 0, 32*t + 32, 32);//?
			m_img.SetSize(MAP_TIP_SIZE,MAP_TIP_SIZE );
            m_img.SetPos(MAP_TIP_SIZE * w - m_scroll.x, MAP_TIP_SIZE * q - m_scroll.y);
            m_img.Draw();
		}
	}
}

int Map::GetTip(const CVector2D& pos, int* tx, int* ty)
{
    //列の計算
    int col = pos.x / MAP_TIP_SIZE;
    //列(0〜MAP_WIDTH-1 まで)の制限
    if (col < 0)col = 0;
    if (col > MAP_WIDTH - 1)col = MAP_WIDTH - 1;
    //行の計算
    int row = pos.y / MAP_TIP_SIZE;
    //列(0〜MAP_HEIGHT-1 まで)の制限
    if (row < 0)row = 0;
    if (row > MAP_HEIGHT - 1)row = MAP_HEIGHT - 1;
    //行と列の出力
    if (ty) *ty = row ;
    if (tx) *tx = col ;
    //チップデータの返却
    return GetTip(col, row);
}

int Map::GetTip(int col, int row)
{
    return m_stage_data[row][col];
}

void Map::SetTip(int col, int row, int t)
{
    m_stage_data[row][col] = t;
}

int Map::CollisionPoint(const CVector2D& pos)
{
    //1点のみ検証
    int t = GetTip(pos);
    if (t != 0)return t;
    return 0;
}

int Map::CollisionRect(const CVector2D& pos, const CRect& rect,CVector2D* rev_pos)
{
    CRect r = CRect(
        pos.x + rect.m_left,
        pos.y + rect.m_top,
        pos.x + rect.m_right,
        pos.y + rect.m_bottom);
    int tx, ty;
    int t;//?
    t = GetTip(CVector2D(r.m_left, r.m_top),&tx,&ty);//&tx,&ty何行目で当たったか
    if (t != 0) {
        rev_pos->y = (ty + 1) * MAP_TIP_SIZE - rect.m_top + 1;//m_top + 1で１マス上
        return t;
    }
    t = GetTip(CVector2D(r.m_right, r.m_top), &tx, &ty);
    if (t != 0) {
        rev_pos->y = (ty + 1) * MAP_TIP_SIZE - rect.m_top + 1;
        return t;
    }
    t = GetTip(CVector2D(r.m_left, r.m_bottom), &tx, &ty);
    if (t != 0) {
        rev_pos->y = ty * MAP_TIP_SIZE - rect.m_bottom - 1;//m_bottom - 1で１マス下
        return t;
    }
    t = GetTip(CVector2D(r.m_right, r.m_bottom), &tx, &ty);
    if (t != 0) {
        rev_pos->y = ty * MAP_TIP_SIZE - rect.m_bottom - 1;
        return t;
    }
    return 0;
}
